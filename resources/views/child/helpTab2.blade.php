<div class="card card-dark">
<div class="card-header">
<h4>Kiosk users</h4>
</div>
<div class="card-body">
<p>Users can start the start the Terminals of their own kiosks.
Only kiosk users (and kiosk admins) can view the logs of a kiosk if the kiosk is set to private.
</p>
<p class="border border-info">Note that by "users" we mean the teachers/staff who use this program to log student attendance. 
We don't mean the actual students; they are not "users".</p>
</div>
